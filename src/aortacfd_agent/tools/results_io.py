_DAT_HEADER_RE = re.compile(r"^#.*$", flags=re.MULTILINE)


def _parse_surface_field_value_dat(text: str) -> List[Dict[str, float]]:
    """Parse an OpenFOAM surfaceFieldValue .dat file into {time, value} records.

    The format is whitespace-delimited with '#' comments. We keep it simple
    because the model only needs enough to answer "what is the peak
    pressure" or "when does diastole start" questions.
    """
    rows: List[Dict[str, float]] = []
    for line in text.splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        parts = line.split()
        if len(parts) < 2:
            continue
        try:
            time = float(parts[0])
            value = float(parts[1])
        except ValueError:
            continue
        rows.append({"time": time, "value": value})
    return rows


def read_pressure_timeseries(args: Dict[str, Any]) -> Dict[str, Any]:
    """Read the pressure time-series for one patch from a finished run.

    Arguments
    ---------
    run_dir : str
    patch : str
        Patch name (e.g. ``'inlet'``, ``'outlet4'``). The tool looks under
        ``postProcessing/<patch>Pressure/0/``.
    """
    run_dir = args.get("run_dir", "")
    patch = str(args.get("patch") or "").strip()
    if not patch:
        return {"error": "patch is required"}

    base = _resolve_run_dir(run_dir)
    if base is None:
        return {"error": f"run_dir does not exist or is not a directory: {run_dir}"}

    pp_root = _first_existing(
        [
            base / "postProcessing",
            base / "openfoam" / "postProcessing",
        ]
    )
    if pp_root is None:
        return {"error": f"no postProcessing directory under {base}"}

    candidates = [
        pp_root / f"{patch}Pressure",
        pp_root / f"{patch}",
    ]
    pressure_dir = _first_existing(candidates)
    if pressure_dir is None:
        return {
            "error": (
                f"no pressure postProcessing folder for patch {patch!r} "
                f"under {pp_root}"
            ),
            "tried": [str(c) for c in candidates],
        }

    # Find any .dat file in the first time-step subdirectory.
    dat_files = sorted(pressure_dir.glob("**/surfaceFieldValue.dat"))
    if not dat_files:
        return {"error": f"no surfaceFieldValue.dat files under {pressure_dir}"}

    dat_file = dat_files[-1]  # use the most recent
    try:
        text = dat_file.read_text(encoding="utf-8")
    except OSError as exc:
        return {"error": f"failed to read {dat_file}: {exc}"}

    rows = _parse_surface_field_value_dat(text)
    if not rows:
        return {"error": f"empty or unreadable time series in {dat_file}"}

    values = [r["value"] for r in rows]
    summary = {
        "num_samples": len(rows),
        "t_start": rows[0]["time"],
        "t_end": rows[-1]["time"],
        "min": min(values),
        "max": max(values),
        "mean": sum(values) / len(values),
    }

    return {
        "run_dir": str(base),
        "patch": patch,
        "path": str(dat_file),
        "summary": summary,
        # Limit the returned series to avoid stuffing the model context
        # window full of noise. 200 evenly-spaced samples are enough for
        # every QA question.
        "series": _downsample(rows, target=200),
    }


def _downsample(rows: List[Dict[str, float]], target: int) -> List[Dict[str, float]]:
    if len(rows) <= target:
        return rows
    step = max(1, len(rows) // target)
    return rows[::step][:target]


def read_pressure_timeseries_spec() -> ToolSpec:
    return ToolSpec(
        name="read_pressure_timeseries",
        description=(
            "Read the pressure time-series for one patch from a finished "
            "CFD run. Returns a downsampled series plus summary statistics "
            "(min, max, mean). Use this for questions like 'what is the "
            "peak inlet pressure?' or 'how large is the systolic spike?'."
        ),
        parameters={
            "type": "object",
            "properties": {
                "run_dir": {"type": "string"},
                "patch": {
                    "type": "string",
                    "description": "Patch name, e.g. 'inlet' or 'outlet4'.",
                },
            },
            "required": ["run_dir", "patch"],
        },
        handler=read_pressure_timeseries,
    )



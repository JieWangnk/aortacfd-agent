def build_results_toolset() -> List[ToolSpec]:
    """Return the tool specs the ResultsAgent exposes to the model."""
    return [
        read_qoi_summary_full_spec(),
        read_hemodynamics_report_spec(),
        read_merged_config_spec(),
        read_pressure_timeseries_spec(),
    ]

"""Read-side tools for the ResultsAgent.

The ResultsAgent answers clinician questions about a finished CFD run
("what is the peak WSS in the coarctation throat?"). To do that safely,
every numerical value in its answer must come from a tool call made in
the current conversation — the model cannot invent numbers. These tools
are the way the model pulls grounded values out of the run directory.

Files read
----------

* ``qoi_summary.json`` / ``results/qoi_summary.json`` — the machine-readable
  summary of the simulation's primary QoIs (WSS percentiles, OSI, pressure
  drop, cycle-averaged values). Already exposed via
  :func:`aortacfd_agent.tools.outputs.read_qoi_summary`; we re-use it here
  for completeness.
* ``hemodynamics_report.txt`` / ``results/hemodynamics_report.txt`` — a
  human-readable text report generated by the CFD post-processor. We
  return the raw text so the model can quote phrases verbatim.
* ``merged_config.json`` / ``reports/merged_config.json`` — the final
  merged config the pipeline actually ran. Useful for answering "what
  settings produced this result".
* Pressure time-series CSVs from ``postProcessing/*Pressure/0/*.dat`` —
  the model can ask for the waveform at a specific patch.

Each tool returns a JSON-serialisable dict and never raises. On any
error, the dict has an ``error`` key — the model can see the error
and try a different tool or a different argument.
"""

from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any, Dict, List, Optional

from ..backends.base import ToolSpec


# ---------------------------------------------------------------------------
# Shared helpers
# ---------------------------------------------------------------------------


def _resolve_run_dir(run_dir: str) -> Optional[Path]:
    p = Path(run_dir).expanduser()
    if not p.exists() or not p.is_dir():
        return None
    return p


def _first_existing(paths: List[Path]) -> Optional[Path]:
    for p in paths:
        if p.exists():
            return p
    return None


# ---------------------------------------------------------------------------
# QoI summary (JSON)
# ---------------------------------------------------------------------------


def read_qoi_summary_full(args: Dict[str, Any]) -> Dict[str, Any]:
    """Return the full qoi_summary.json contents for a finished run.

    This is essentially the same tool as
    :func:`aortacfd_agent.tools.outputs.read_qoi_summary`, but with a
    slightly richer return shape: it includes the discovered file path
    so the model can cite it.
    """
    run_dir = args.get("run_dir", "")
    base = _resolve_run_dir(run_dir)
    if base is None:
        return {"error": f"run_dir does not exist or is not a directory: {run_dir}"}

    candidate = _first_existing(
        [
            base / "results" / "qoi_summary.json",
            base / "qoi_summary.json",
            base / "reports" / "qoi_summary.json",
        ]
    )
    if candidate is None:
        return {"error": f"no qoi_summary.json found under {base}"}

    try:
        data = json.loads(candidate.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        return {"error": f"failed to parse {candidate}: {exc}"}

    return {
        "run_dir": str(base),
        "path": str(candidate),
        "qoi": data,
    }


def read_qoi_summary_full_spec() -> ToolSpec:
    return ToolSpec(
        name="read_qoi_summary",
        description=(
            "Read the full qoi_summary.json for a finished CFD run. "
            "Returns the parsed JSON plus the discovered file path. Call "
            "this first when answering any numerical question — it is "
            "the cheapest way to get the primary hemodynamic indices."
        ),
        parameters={
            "type": "object",
            "properties": {
                "run_dir": {
                    "type": "string",
                    "description": "Absolute or relative path to the finished run directory.",
                }
            },
            "required": ["run_dir"],
        },
        handler=read_qoi_summary_full,
    )


